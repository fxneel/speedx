					<div class='item-data'>
							<p>Documentação Ortodôntica/Ortopédica: É um conjunto de exames que permite ao dentista avaliar seu paciente quanto ao crescimento ósseo da maxila/mandíbula e da face, visando à movimentação correta e harmoniosa dos dentes. Os exames mais pedidos nas documentações ortodônticas/ ortopédicas são:</p>
